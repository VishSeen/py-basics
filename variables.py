# Variables 
# ------------ #

# variables are used to store data (values).
# they are like boxes and hold values assigned.
# variables can hold different types of values
# ex: integers (Whole numbers), floats (decimal numbers)
# strings (Text), boolean (true or false) etc
# variables can be changed later. 
# they should always be assigned a value when creating.
# variable makes it easy to point to certain values.


# To create a variable
# first: give it a name.
# sec : use the = (equal sign) to assign a value (called value assignment)
# third : enter the value you want to addd
# ---------------------------- #
# a_name_you_want = your value #
# ---------------------------- #


# here x is the name, 
# equal sign shows that a value is gonna come after 
# finally 1 added as the value
x = 1

# creating integer variable
y = 4


# creating float variable
z = 3.14


# creating string variables
name = "Vishroy"
another_name = "Shane"
a_third_name = "Seenarains"


# creating boolean variables
is_true = True
is_false = False


# creating a variable and changing its value.
animal = "dog"
print(animal) # current value in animal

animal = "cat"
print(animal) # value after changing



# creating multiple variable and assigninig multiple values
# you can create multiple variable at once
# by separating your different variable names with
# a comma (,). 
# for ex: below we are creating 2 variable named : a and another one b.
# a holds the value : 5 
# b holds the value : 10
# values should be separated by comma and make sure to add as many 
# values as the variables you have on left hand side.
a, b = 5, 10
print(a)
print(b)

# creating multiple variable with same values
# when printing, all 3 will hold the same text
m, n , o = "same value for all three variables"
print(m)
print(n)
print(o)



